
public class Hand
	{
	String color, purpose;
	int value;
	public Hand(String c, String p, int v)
		{
		color=c;
		purpose =p;
		value=v;
		}
	public String getColor()
		{
		return color;
		}
	public String getPurpose()
		{
		return purpose;
		}
	public int getValue()
		{
		return value;
		}
	}
